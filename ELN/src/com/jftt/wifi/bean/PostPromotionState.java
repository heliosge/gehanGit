package com.jftt.wifi.bean;

import java.util.Date;

/**
 * 岗位晋升记录表
 * @author zhangbocheng
 *
 */
public class PostPromotionState {
	private Integer id; //主键
	private Integer userId;//用户id
	private Integer promotionStageId;//晋升路线阶段
	private Integer isExamPassed;//考试是否通过
	private Integer examScore;//考试分数
	private Integer isCourseFinished;//阶段关联学分是否达标
	private Integer obligatoryCredits;//必修课实际获得学分
	private Integer electiveCredits;//选修课实际获得学分
	private Integer promotionApproveStatus;//晋升审批状态：1：未达到晋升标准，2：待审批，3：审批不通过，4：审批通过
	private String promotionRefuseReason;//晋升审批审批不通过理由
	private Integer reStartApproveStatus;//申请重新学习的审批状态：1：未申请，2：待审批，3：审批不通过，4：审批通过
	private String reStartRefuseReason;//重新学习申请审批不通过理由
	private Date reStartTime;//重新学习时间
	private Integer promotionStatus;//晋升状态：1：晋升成功，2：晋升中，3：晋升失败
	private Date updateTime;//更新时间
	private Date stageBeginTime;//阶段开始时间
	private String promotionSucceedTime;//晋升成功时间
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getPromotionStageId() {
		return promotionStageId;
	}
	public void setPromotionStageId(Integer promotionStageId) {
		this.promotionStageId = promotionStageId;
	}
	public Integer getIsExamPassed() {
		return isExamPassed;
	}
	public void setIsExamPassed(Integer isExamPassed) {
		this.isExamPassed = isExamPassed;
	}
	public Integer getExamScore() {
		return examScore;
	}
	public void setExamScore(Integer examScore) {
		this.examScore = examScore;
	}
	public Integer getIsCourseFinished() {
		return isCourseFinished;
	}
	public void setIsCourseFinished(Integer isCourseFinished) {
		this.isCourseFinished = isCourseFinished;
	}
	public Integer getObligatoryCredits() {
		return obligatoryCredits;
	}
	public void setObligatoryCredits(Integer obligatoryCredits) {
		this.obligatoryCredits = obligatoryCredits;
	}
	public Integer getElectiveCredits() {
		return electiveCredits;
	}
	public void setElectiveCredits(Integer electiveCredits) {
		this.electiveCredits = electiveCredits;
	}
	public Integer getPromotionApproveStatus() {
		return promotionApproveStatus;
	}
	public void setPromotionApproveStatus(Integer promotionApproveStatus) {
		this.promotionApproveStatus = promotionApproveStatus;
	}
	public String getPromotionRefuseReason() {
		return promotionRefuseReason;
	}
	public void setPromotionRefuseReason(String promotionRefuseReason) {
		this.promotionRefuseReason = promotionRefuseReason;
	}
	public Integer getReStartApproveStatus() {
		return reStartApproveStatus;
	}
	public void setReStartApproveStatus(Integer reStartApproveStatus) {
		this.reStartApproveStatus = reStartApproveStatus;
	}
	public String getReStartRefuseReason() {
		return reStartRefuseReason;
	}
	public void setReStartRefuseReason(String reStartRefuseReason) {
		this.reStartRefuseReason = reStartRefuseReason;
	}
	public Date getReStartTime() {
		return reStartTime;
	}
	public void setReStartTime(Date reStartTime) {
		this.reStartTime = reStartTime;
	}
	public Integer getPromotionStatus() {
		return promotionStatus;
	}
	public void setPromotionStatus(Integer promotionStatus) {
		this.promotionStatus = promotionStatus;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public Date getStageBeginTime() {
		return stageBeginTime;
	}
	public void setStageBeginTime(Date stageBeginTime) {
		this.stageBeginTime = stageBeginTime;
	}
	public String getPromotionSucceedTime() {
		return promotionSucceedTime;
	}
	public void setPromotionSucceedTime(String promotionSucceedTime) {
		this.promotionSucceedTime = promotionSucceedTime;
	}
	
	
	
}
