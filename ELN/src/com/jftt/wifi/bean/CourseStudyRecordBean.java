public class CourseStudyRecordBean {
	private Integer id;//学习进度id
	private Integer courseId;//课程id
	private Integer userId;//学习人id
	private Integer companyId;//公司id
	private Integer subCompanyId;//子公司id
	private Integer learnProcess;//学习进度（1：进行中 2：已完成）
	private Date startTime;//开始学习时间
	private Date endTime;//结束学习时间
	private Date thisLearnTime;//本次学习时间
	/*add by luyunlong */
	private ResCourseBean course;//课程bean
	
	public ResCourseBean getCourse() {
		return course;
	}
	public void setCourse(ResCourseBean course) {
		this.course = course;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCourseId() {
		return courseId;
	}
	public void setCourseId(Integer courseId) {
		this.courseId = courseId;
	}
	public Integer getCompanyId() {
		return companyId;
	}
	public void setCompanyId(Integer companyId) {
		this.companyId = companyId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getLearnProcess() {
		return learnProcess;
	}
	public void setLearnProcess(Integer learnProcess) {
		this.learnProcess = learnProcess;
	}
	public Date getStartTime() {
		return startTime;
	}
	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
	public Date getThisLearnTime() {
		return thisLearnTime;
	}
	public void setThisLearnTime(Date thisLearnTime) {
		this.thisLearnTime = thisLearnTime;
	}
	public Integer getSubCompanyId() {
		return subCompanyId;
	}
	public void setSubCompanyId(Integer subCompanyId) {
		this.subCompanyId = subCompanyId;
	}
}
