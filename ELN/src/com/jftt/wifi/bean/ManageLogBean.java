package com.jftt.wifi.bean;

import java.io.Serializable;

/**
 * 操作日志
 */
public class ManageLogBean implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -4231184811533921133L;
	
	
	private long id; 		         //主键ID
	private int type;                //类型
	private long operateId;          //操作人
	private String operateTime;      //操作时间
	private String content;          //操作内容
	private String operateTable;     //操作的表名
	private String operateDataId;    //操作的数据主键
	
	//用于查询,无实际意义
	private String operateTimeEnd;
	//操作人姓名，用于展示
	private String operateName; 
	//用于分页查询,无实际意义
	private String fromNum;
	private String pageSize;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public long getOperateId() {
		return operateId;
	}
	public void setOperateId(long operateId) {
		this.operateId = operateId;
	}
	public String getOperateTime() {
		return operateTime;
	}
	public void setOperateTime(String operateTime) {
		this.operateTime = operateTime;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getOperateTimeEnd() {
		return operateTimeEnd;
	}
	public void setOperateTimeEnd(String operateTimeEnd) {
		this.operateTimeEnd = operateTimeEnd;
	}
	public String getOperateName() {
		return operateName;
	}
	public void setOperateName(String operateName) {
		this.operateName = operateName;
	}
	public String getFromNum() {
		return fromNum;
	}
	public void setFromNum(String fromNum) {
		this.fromNum = fromNum;
	}
	public String getPageSize() {
		return pageSize;
	}
	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}
	public String getOperateTable() {
		return operateTable;
	}
	public void setOperateTable(String operateTable) {
		this.operateTable = operateTable;
	}
	public String getOperateDataId() {
		return operateDataId;
	}
	public void setOperateDataId(String operateDataId) {
		this.operateDataId = operateDataId;
	}
	
}
