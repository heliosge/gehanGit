package com.jftt.wifi.bean.ask;

import java.util.Date;

/**
 * class name:AskVoForMMGrid <BR>
 * class description: 前台列表展现问问 <BR>
 * Remark: <BR>
 * @version 1.00 2015年12月10日
 * @author JFTT)ShenHaijun
 */
public class AskVoForMMGrid {
	private Integer id;//问问id
	private String title;//标题
	private Date createTime;//提问时间
	private Date effectiveTime;//问题截止有效期
	private Integer answerCount;//回答数量
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getEffectiveTime() {
		return effectiveTime;
	}
	public void setEffectiveTime(Date effectiveTime) {
		this.effectiveTime = effectiveTime;
	}
	public Integer getAnswerCount() {
		return answerCount;
	}
	public void setAnswerCount(Integer answerCount) {
		this.answerCount = answerCount;
	}
}
