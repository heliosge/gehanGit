package com.jftt.wifi.bean;

import java.util.Date;


/**
 * class LearnCourseProcessBean <BR>
 * class description: 课程学习进度bean <BR>
 * Remark: <BR>
 * @version 1.00 2015-8-6
 * @author zhangbocheng
 * 
 */
public class LearnCourseProcessBean {

	private Integer id; //课程学习进度id
	private Integer userId; //用户id
	private Integer courseId; //课程id
    private Integer learnProcess;//学习进度
    private Date lastLearnTime;//上次学习时间
    private Date beginTime;//开始学习时间
    private Date endTime;//结束学习时间
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getCourseId() {
		return courseId;
	}
	public void setCourseId(Integer courseId) {
		this.courseId = courseId;
	}
	public Integer getLearnProcess() {
		return learnProcess;
	}
	public void setLearnProcess(Integer learnProcess) {
		this.learnProcess = learnProcess;
	}
	public Date getLastLearnTime() {
		return lastLearnTime;
	}
	public void setLastLearnTime(Date lastLearnTime) {
		this.lastLearnTime = lastLearnTime;
	}
	public Date getBeginTime() {
		return beginTime;
	}
	public void setBeginTime(Date beginTime) {
		this.beginTime = beginTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
    
    
}
