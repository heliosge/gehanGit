public class ExamGetScoreVo {
	private int type;//题型
	private int correctNum;//正确题数
	private int wrongNum;//错误题数
	private int unwriteNum;//未作答题数
	private int score;//每种题型得分
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getCorrectNum() {
		return correctNum;
	}
	public void setCorrectNum(int correctNum) {
		this.correctNum = correctNum;
	}
	public int getWrongNum() {
		return wrongNum;
	}
	public void setWrongNum(int wrongNum) {
		this.wrongNum = wrongNum;
	}
	public int getUnwriteNum() {
		return unwriteNum;
	}
	public void setUnwriteNum(int unwriteNum) {
		this.unwriteNum = unwriteNum;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
