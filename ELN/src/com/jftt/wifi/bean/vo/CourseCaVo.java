public class CourseCaVo extends CommonPagingVo{
	private String userId;
	private String companyId;
	private String subCompanyId;
	private String categoryId;
	private String name;//搜索名称
	private String isBuy;//是否显示已购买的  1 显示
	private String orderType;//排序类型 1 时间 2销量 3评价
	private String orderRule;//排序规则  可选值：asc  desc（默认值） 
	private String showFree;//是否只显示免费课程  1是 0 否
	
	private String childCategoryIds;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getCompanyId() {
		return companyId;
	}
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	public String getSubCompanyId() {
		return subCompanyId;
	}
	public void setSubCompanyId(String subCompanyId) {
		this.subCompanyId = subCompanyId;
	}
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIsBuy() {
		return isBuy;
	}
	public void setIsBuy(String isBuy) {
		this.isBuy = isBuy;
	}
	public String getOrderType() {
		return orderType;
	}
	public void setOrderType(String orderType) {
		this.orderType = orderType;
	}
	public String getOrderRule() {
		return orderRule;
	}
	public void setOrderRule(String orderRule) {
		this.orderRule = orderRule;
	}
	public String getShowFree() {
		return showFree;
	}
	public void setShowFree(String showFree) {
		this.showFree = showFree;
	}
	public String getChildCategoryIds() {
		return childCategoryIds;
	}
	public void setChildCategoryIds(String childCategoryIds) {
		this.childCategoryIds = childCategoryIds;
	}
	
	
	
}
