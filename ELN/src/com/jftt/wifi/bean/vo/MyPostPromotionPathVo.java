public class MyPostPromotionPathVo {
	private Integer id;//路径id
	private String name;//路径名称
	private Integer isDisabled;//是否停用：1停用，0启用
	private Integer promotionState;//晋升百分比
	private Integer currentStageId;//当前所处阶段id
	
	public Integer getCurrentStageId() {
		return currentStageId;
	}
	public void setCurrentStageId(Integer currentStageId) {
		this.currentStageId = currentStageId;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getIsDisabled() {
		return isDisabled;
	}
	public void setIsDisabled(Integer isDisabled) {
		this.isDisabled = isDisabled;
	}
	public Integer getPromotionState() {
		return promotionState;
	}
	public void setPromotionState(Integer promotionState) {
		this.promotionState = promotionState;
	}
}
