package com.jftt.wifi.bean.vo;

import java.util.Date;

/**
 * class name:MyQuestionVo <BR>
 * class description: 我的问答vo <BR>
 * Remark: <BR>
 * @version 1.00 2015年9月1日
 * @author JFTT)ShenHaijun
 */
public class MyQuestionVo {
	private Integer courseId;//课程id
	private String courseName;//课程名称
	private String courseCode;//课程编号
	private String frontImage;//课程封面图片
	private Date lastAskTime;//最近的提问时间
	private Integer questionCount;//该门课程该学员的提问条数
	
	public Integer getCourseId() {
		return courseId;
	}
	public void setCourseId(Integer courseId) {
		this.courseId = courseId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseCode() {
		return courseCode;
	}
	public void setCourseCode(String courseCode) {
		this.courseCode = courseCode;
	}
	public String getFrontImage() {
		return frontImage;
	}
	public void setFrontImage(String frontImage) {
		this.frontImage = frontImage;
	}
	public Date getLastAskTime() {
		return lastAskTime;
	}
	public void setLastAskTime(Date lastAskTime) {
		this.lastAskTime = lastAskTime;
	}
	public Integer getQuestionCount() {
		return questionCount;
	}
	public void setQuestionCount(Integer questionCount) {
		this.questionCount = questionCount;
	}
}
