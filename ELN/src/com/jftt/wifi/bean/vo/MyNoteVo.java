package com.jftt.wifi.bean.vo;

import java.util.Date;

/**
 * class name:MyNoteVo <BR>
 * class description: 我的笔记vo <BR>
 * Remark: <BR>
 * @version 1.00 2015年8月30日
 * @author JFTT)ShenHaijun
 */
public class MyNoteVo {
	private Integer courseId;//课程id
	private String courseName;//课程名
	private String courseFrontImage;//课程封面
	private String courseCode;//课程编号
	private Integer noteCount;//该门课的我的笔记数量
	private Date noteDate;//笔记的最晚时间
	
	public Integer getCourseId() {
		return courseId;
	}
	public void setCourseId(Integer courseId) {
		this.courseId = courseId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseFrontImage() {
		return courseFrontImage;
	}
	public void setCourseFrontImage(String courseFrontImage) {
		this.courseFrontImage = courseFrontImage;
	}
	public String getCourseCode() {
		return courseCode;
	}
	public void setCourseCode(String courseCode) {
		this.courseCode = courseCode;
	}
	public Integer getNoteCount() {
		return noteCount;
	}
	public void setNoteCount(Integer noteCount) {
		this.noteCount = noteCount;
	}
	public Date getNoteDate() {
		return noteDate;
	}
	public void setNoteDate(Date noteDate) {
		this.noteDate = noteDate;
	}
}
