package com.jftt.wifi.bean.vo;

import java.util.List;

/**
 * class name:CourseNextTypeVo <BR>
 * class description: 二级课程体系或分类vo <BR>
 * Remark: <BR>
 * @version 1.00 2015年11月12日
 * @author JFTT)ShenHaijun
 */
public class CourseNextTypeVo {
	private Integer nextId;//体系或者分类id
	private String nextName;//体系或者分类名称
	private Integer nextCategoryOrType;//体系还是分类（1：体系，2：分类）
	private List<CourseTypeVo> thirdTypes;//三级课程体系或者分类目录
	
	public Integer getNextId() {
		return nextId;
	}
	public void setNextId(Integer nextId) {
		this.nextId = nextId;
	}
	public String getNextName() {
		return nextName;
	}
	public void setNextName(String nextName) {
		this.nextName = nextName;
	}
	public Integer getNextCategoryOrType() {
		return nextCategoryOrType;
	}
	public void setNextCategoryOrType(Integer nextCategoryOrType) {
		this.nextCategoryOrType = nextCategoryOrType;
	}
	public List<CourseTypeVo> getThirdTypes() {
		return thirdTypes;
	}
	public void setThirdTypes(List<CourseTypeVo> thirdTypes) {
		this.thirdTypes = thirdTypes;
	}
}
