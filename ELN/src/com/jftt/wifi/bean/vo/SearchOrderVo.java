public class SearchOrderVo extends CommonPagingVo{
	private String userId;
	private String orderCode;
	private String courseName;
	private String priceFrom;//筛选价格From
	private String priceTo;//筛选价格To
	private String orderCreateStartTime;//下单开始时间
	private String orderCreateEndTime;//下单结束时间
	private String buyStatus;//购买状态
	private String invoiceStatus;//发票状态
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getOrderCode() {
		return orderCode;
	}
	public void setOrderCode(String orderCode) {
		this.orderCode = orderCode;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getPriceFrom() {
		return priceFrom;
	}
	public void setPriceFrom(String priceFrom) {
		this.priceFrom = priceFrom;
	}
	public String getPriceTo() {
		return priceTo;
	}
	public void setPriceTo(String priceTo) {
		this.priceTo = priceTo;
	}
	public String getOrderCreateStartTime() {
		return orderCreateStartTime;
	}
	public void setOrderCreateStartTime(String orderCreateStartTime) {
		this.orderCreateStartTime = orderCreateStartTime;
	}
	public String getOrderCreateEndTime() {
		return orderCreateEndTime;
	}
	public void setOrderCreateEndTime(String orderCreateEndTime) {
		this.orderCreateEndTime = orderCreateEndTime;
	}
	public String getBuyStatus() {
		return buyStatus;
	}
	public void setBuyStatus(String buyStatus) {
		this.buyStatus = buyStatus;
	}
	public String getInvoiceStatus() {
		return invoiceStatus;
	}
	public void setInvoiceStatus(String invoiceStatus) {
		this.invoiceStatus = invoiceStatus;
	}
	
}
